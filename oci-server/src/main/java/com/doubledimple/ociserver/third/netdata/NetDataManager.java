    public boolean uninstallNetData() {
        try {
            log.warn("开始卸载NetData...");

            // 停止服务
            ProcessBuilder stopPb = new ProcessBuilder("sudo", "systemctl", "stop", "netdata");
            stopPb.start().waitFor();

            // 禁用服务
            ProcessBuilder disablePb = new ProcessBuilder("sudo", "systemctl", "disable", "netdata");
            disablePb.start().waitFor();

            // 卸载（根据安装方式选择）
            ProcessBuilder uninstallPb = new ProcessBuilder("sudo", "apt", "remove", "-y", "netdata");
            Process uninstallProcess = uninstallPb.start();
            boolean success = uninstallProcess.waitFor() == 0;

            if (success) {
                log.info("NetData卸载完成");
            } else {
                log.error("NetData卸载失败");
            }

            return success;

        } catch (Exception e) {
            log.error("卸载NetData失败", e);
            return false;
        }
    }
