    /**
     * 验证Master节点是否正常工作
     */
    public boolean validateMasterNode() {
        try {
            // 检查端口是否开放
            ProcessBuilder pb = new ProcessBuilder("netstat", "-tlnp");
            Process process = pb.start();

            try (BufferedReader reader = new BufferedReader(new InputStreamReader(process.getInputStream()))) {
                String line;
                while ((line = reader.readLine()) != null) {
                    if (line.contains(":" + NETDATA_PORT)) {
                        log.info("NetData端口{}已开放", NETDATA_PORT);
                        return true;
                    }
                }
            }

            log.warn("NetData端口{}未开放", NETDATA_PORT);
            return false;

        } catch (Exception e) {
            log.error("验证Master节点失败", e);
            return false;
        }
    }

    /**
     * 获取NetData API信息
     */
    public String getNetDataApiInfo() {
        try {
            ProcessBuilder pb = new ProcessBuilder("curl", "-s",
                    getMasterUrl() + "/api/v1/info");
            Process process = pb.start();

            try (BufferedReader reader = new BufferedReader(new InputStreamReader(process.getInputStream()))) {
                StringBuilder response = new StringBuilder();
                String line;
                while ((line = reader.readLine()) != null) {
                    response.append(line);
                }
                return response.toString();
            }
        } catch (Exception e) {
            log.error("获取NetData API信息失败", e);
            return null;
        }
    }

    /**
     * 一键在远程VPS上安装并配置Child节点
     *
     * @param host VPS主机地址
     * @param username SSH用户名
     * @param password SSH密码
     * @param port SSH端口
     * @return 安装配置是否成功
     */
    public boolean installChildNodeOnVPS(String masterAddress,String masterApiKey,String host, String username, String password, int port) {
        try {
            log.info("开始在VPS {}:{} 安装NetData Child节点...", host, port);

            // 检查Master节点状态
            if (masterApiKey == null || masterApiKey.isEmpty()) {
                log.error("Master节点未初始化或API Key为空，请先安装Master节点");
                return false;
            }

            // 生成安装和配置脚本
            String installScript = generateChildInstallScript(masterAddress,masterApiKey);
            log.info("生成Child节点安装脚本完成");

            // 通过SSH执行脚本
            log.info("通过SSH连接到 {}:{} 执行安装脚本...", host, port);
            ScriptResult result = JschUtils.executeScriptJsch(host, username, password, port, installScript);

            // 检查执行结果
            if (result.isSuccess() && result.getOutput() != null && result.getOutput().contains("Child节点配置成功")) {
                log.info("VPS {} Child节点安装配置成功", host);
                log.info("执行输出: {}", result.getOutput());
                return true;
            } else {
                log.error("VPS {} Child节点安装配置失败", host);
                log.error("退出码: {}", result.getExitCode());
                log.error("标准输出: {}", result.getOutput());
                log.error("错误输出: {}", result.getError());
                return false;
            }

        } catch (Exception e) {
            log.error("安装Child节点失败，VPS: {}:{}", host, port, e);
            return false;
        }
    }

    /**
     * 批量安装Child节点
     *
     * @param vpsInfos VPS信息列表
     * @return 成功安装的数量
     */
    public int batchInstallChildNodes(String masterAddress,String masterApiKey,List<VpsInfo> vpsInfos) {
        int successCount = 0;

        log.info("开始批量安装 {} 个Child节点...", vpsInfos.size());

        for (VpsInfo vps : vpsInfos) {
            try {
                log.info("正在处理VPS: {}", vps.getHost());

                boolean success = installChildNodeOnVPS(masterAddress,masterApiKey,
                        vps.getHost(),
                        vps.getUsername(),
                        vps.getPassword(),
                        vps.getPort()
                );

                if (success) {
                    successCount++;
                    log.info("VPS {} 安装成功 ({}/{})", vps.getHost(), successCount, vpsInfos.size());
                } else {
                    log.warn("VPS {} 安装失败", vps.getHost());
                }

                // 避免并发过多，稍作延迟
                Thread.sleep(2000);

            } catch (Exception e) {
                log.error("处理VPS {}失败", vps.getHost(), e);
            }
        }

        log.info("批量安装完成，成功: {}, 失败: {}", successCount, vpsInfos.size() - successCount);
        return successCount;
    }

    /**
     * 仅配置已安装NetData的VPS为Child节点
     * 适用于已安装NetData但未配置为Child的VPS
     */
    public boolean configureExistingNetDataAsChild(String host, String username, String password, int port) {
        try {
            log.info("配置已安装的NetData为Child节点: {}:{}", host, port);

            if (masterApiKey == null || masterApiKey.isEmpty()) {
                log.error("Master节点未初始化，请先安装Master节点");
                return false;
            }

            // 生成配置脚本（不包含安装）
            String configScript = generateChildConfigScript();

            // 执行配置脚本
            ScriptResult result = JschUtils.executeScriptJsch(host, username, password, port, configScript);

            if (result.isSuccess() && result.getOutput() != null && result.getOutput().contains("Child节点配置完成")) {
                log.info("VPS {} Child节点配置成功", host);
                log.info("执行输出: {}", result.getOutput());
                return true;
            } else {
                log.error("VPS {} Child节点配置失败", host);
                log.error("退出码: {}", result.getExitCode());
                log.error("标准输出: {}", result.getOutput());
                log.error("错误输出: {}", result.getError());
                return false;
            }

        } catch (Exception e) {
            log.error("配置Child节点失败: {}:{}", host, port, e);
            return false;
        }
    }

